# todo:  Даны три переменные: X, Y, Z. Их значения числа.
# Из данных произвольных чисел выбрать наибольшее.

# Пример:
# X = 5
# Y = 10
# Z = 3
# Ответ: Наибольшее число 10.
#
# X = 10
# Y = 12
# Z = -7
# Ответ: Наибольшее число 12.

x = 5
y = 10
z = 3
print("Ответ: Наибольшее число ", max(x, y, z))

x = 10
y = 12
z = -7
print("Ответ: Наибольшее число ", max(x, y, z))