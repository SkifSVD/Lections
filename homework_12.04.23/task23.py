#  Отсортируйте этот список по возрастанию общего количества цифр в каждом списке.
#  Каждый список отсортируйте по убыванию.

a = [[1,5,3], [2,44,1,4], [3,3]]

i = 0
while i < len(a):
    if i == len(a) - 1:
        break
    if len(a[i]) < len(a[i+1]):
        tmp = a.pop(i+1)
        a.insert(i, tmp)
    i += 1


print(a)